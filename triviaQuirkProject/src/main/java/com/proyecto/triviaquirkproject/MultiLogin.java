    private boolean validateName(String username){
        String regex = "^[a-zA-Z0-9]+$";

        // Verificar si el nombre del jugador cumple con la expresiÃ³n regular
        
        if ((username.matches(regex))){
            if (username.matches("Jugador")){
            return false;
            }       
            return true;
        } else {
            return false;
        }
    }
    
